#!/usr/bin/env node
/**
 * Generate algorithm defaults from QSM.rs WASM module.
 *
 * This script loads the built WASM, calls get_*_defaults() functions,
 * and writes the values to js/app/qsm-defaults.js as static exports.
 * Run after wasm-pack build.
 */

import { readFile, writeFile } from 'fs/promises';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, '..');

// Load WASM module
const wasmJsPath = join(ROOT, 'rust-wasm', 'pkg', 'qsm_wasm.js');
const wasmBinaryPath = join(ROOT, 'rust-wasm', 'pkg', 'qsm_wasm_bg.wasm');

const wasmModule = await import(wasmJsPath);
const wasmBinary = await readFile(wasmBinaryPath);
await wasmModule.default(wasmBinary);

// Collect defaults from WASM
const defaults = {
  RTS_DEFAULTS: JSON.parse(wasmModule.get_rts_defaults()),
  TV_DEFAULTS: JSON.parse(wasmModule.get_tv_defaults()),
  TKD_DEFAULTS: JSON.parse(wasmModule.get_tkd_defaults()),
  TGV_DEFAULTS: JSON.parse(wasmModule.get_tgv_defaults()),
  BET_DEFAULTS: JSON.parse(wasmModule.get_bet_defaults()),
  VSHARP_DEFAULTS: JSON.parse(wasmModule.get_vsharp_defaults()),
  PDF_DEFAULTS: JSON.parse(wasmModule.get_pdf_defaults()),
  LBV_DEFAULTS: JSON.parse(wasmModule.get_lbv_defaults()),
  ISMV_DEFAULTS: JSON.parse(wasmModule.get_ismv_defaults()),
  SWI_DEFAULTS: JSON.parse(wasmModule.get_swi_defaults()),
  SHARP_DEFAULTS: JSON.parse(wasmModule.get_sharp_defaults()),
  RESHARP_DEFAULTS: JSON.parse(wasmModule.get_resharp_defaults()),
  HARPERELLA_DEFAULTS: JSON.parse(wasmModule.get_harperella_defaults()),
  TIKHONOV_DEFAULTS: JSON.parse(wasmModule.get_tikhonov_defaults()),
  NLTV_DEFAULTS: JSON.parse(wasmModule.get_nltv_defaults()),
  MEDI_DEFAULTS: JSON.parse(wasmModule.get_medi_defaults()),
  QSMART_DEFAULTS: JSON.parse(wasmModule.get_qsmart_defaults()),
  ROMEO_DEFAULTS: JSON.parse(wasmModule.get_romeo_defaults()),
  MCPC3DS_DEFAULTS: JSON.parse(wasmModule.get_mcpc3ds_defaults()),
  LINEAR_FIT_DEFAULTS: JSON.parse(wasmModule.get_linear_fit_defaults()),
  HOMOGENEITY_DEFAULTS: JSON.parse(wasmModule.get_homogeneity_defaults()),
};

// Generate JS file
const lines = [
  '/**',
  ' * Algorithm defaults from QSM.rs — AUTO-GENERATED by scripts/generate-defaults.mjs',
  ' * Do not edit manually. Run `./build.sh` to regenerate.',
  ` * Generated from QSM.rs WASM (qsm-core)`,
  ' */',
  '',
];

for (const [name, obj] of Object.entries(defaults)) {
  lines.push(`export const ${name} = ${JSON.stringify(obj, null, 2)};`);
  lines.push('');
}

const outputPath = join(ROOT, 'js', 'app', 'qsm-defaults.js');
await writeFile(outputPath, lines.join('\n'));

console.log(`Generated ${outputPath}`);
for (const [name, obj] of Object.entries(defaults)) {
  console.log(`  ${name}: ${JSON.stringify(obj)}`);
}
